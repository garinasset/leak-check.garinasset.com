export default function QueryTypeDisplay({ normalizedQuery }: QueryTypeDisplayProps) {
    const queryType = detectPersonQueryType(normalizedQuery);
    let content: React.ReactNode = null;

    if (queryType === "idCard") {
        content = "🪪";
    }
    else if (queryType === "phone") {
        content = "📞";
    }
    else if (queryType === "email") {
        content = "✉️";
    }
    else if (queryType === "qq") {
        content = <QQIcon />;
    }
    else {
        content = <UnknownIcon />;
    }

    return (
        <div className="w-5 h-5 flex items-center justify-center">
            {typeof content === "string" ? (
                <span className="text-base leading-none">{content}</span>
            ) : (
                content
            )}
        </div>
    );
}
